window.itemChecked = function(element, listIndex, itemIndex){
  
  $(element).parent().toggleClass("strike");
  let checkValue = !LISTS[listIndex].listItems[itemIndex].checked;
  LISTS[listIndex].listItems[itemIndex].checked = checkValue;
  
  // console.log("Checked" + LISTS[listIndex].listItems[itemIndex].checked);

}

window.addItem = function(listIndex){
  let newItemName = $("#addItem").val();
  let newItemObj = {
    name: newItemName,
    checked: false,
    category: "",
  };

  LISTS[listIndex].listItems.push(newItemObj);
  loadListItems(listIndex);
}

window.deleteItem = function(listIndex, idx) {
  LISTS[listIndex].listItems.splice(idx,1);
  loadListItems(listIndex);
}

window.loadListItems = function(listIndex){
  console.log("this is inside window.loadListItems " + listIndex );
  

  let listString = `<button class="btn" onclick="loadMoreItems()">Back</button><ul>`;
  $.each(LISTS[listIndex].listItems, function(idx, listItem) {
    listString += `<li id="${idx}" class="${listItem.checked ? "strike": ""}"><input ${listItem.checked ? (listItem.checked = "checked"): "" } type="checkbox" id="${idx}" name="${listItem.name}" onclick="itemChecked(this, ${listIndex}, ${idx})">
    <span>${listItem.name}</span>
    <span class="delete" onclick="deleteItem(${listIndex}, ${idx})">Delete</span>
    </li>`;
});
listString += `</ul>
<div class="addItemInput">
<input id="addItem" type="text" placeholder="-----------">
<button class="btn" onclick="addItem(${listIndex})">Add Item</button>
</div>`;


// loads images base on the list of items click
$("#app").html(`
  <div class="items-container">
    <div class="items-items">
    ${listString}
    </div>  
    
    <div class="item-img">
      <img src="images/camping${listIndex}.jpg" alt=""/>
    </div>  
  </div>`);
}

